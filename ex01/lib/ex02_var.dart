void main (){
  // 변수 타입
  // 직접 변수 타입을 명시하면 [직관적]이고 [유지 보수가 용이]함

  // String - 문자열
  String name = '코드팩토리';

  // int - 정수
  int isInt = 10;

  // double - 실수
  double isDouble = 2.5;

  // bool - 불리언 (true/false)
  bool isTrue = true;

  print(name);
  print(isInt);
  print(isDouble);
  print(isTrue);
}